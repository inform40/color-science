/**
 * The different Coler Code Types
 */
export enum ColorCode {
    /**
     * Hexadecimal code of length 3
     */
    Hex3,
    /**
     * Hexadecimal code of length 6
     */
    Hex6,
    /**
     * Red Green Blue
     */
    Rgb,
    /**
     * Red Green Blue with Transparency
     */
    Rgba,
}
